package OOPs;

class EmployeeInfo {

	private int empID;
	private String empName;
	
	// getter method
	public int getEmpID() {
		return empID;
	}
	
	//setter methoed
	public void setEmpID(int empID) {
		this.empID = empID;
	}
	
	public String getEmpName() {
		return empName;
	}
	
	public void setEmpName(String empName) {
		this.empName = empName;
	}


}
